# En python existen distintas operaciones para manipular una lista.

# Concatenacion de listas:

print("Concatenacion:")

lista1 = ["hola","como"]
lista2 = ["te","llamas"]

print(lista1 + lista2)

# Busqueda:

print("Busqueda:")

lista1 = ["Arroz","Huevo"]
