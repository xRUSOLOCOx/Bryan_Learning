# En todo lenguaje existen diferentes tipos de datos:

# String, str, cadena de texto:

"Hola mundo"
'Hola mundo'

# integer, Int, entero:

-12
6
5

# Float, float, flotante:

5.5
2.5
-0.9

# Boolean, bool, Booleano:

True
False

#Consultar el tipo de datos con la funcion Type:

print(type("Hola mundo")) # Tipo str
print(type(5)) # Tipo int
print(type(2.5)) # Tipo float
print(type(True)) # Tipo bool

