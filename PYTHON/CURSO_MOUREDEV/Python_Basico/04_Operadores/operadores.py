### Operadores ###

### Operadores Aritmeticos: ###

print(3 + 4)   # Suma
print(3 - 4)   # Resta
print(3 * 4)   # Multiplicacion
print(3 / 4)   # Division
print(3 ** 4)  # exponenciacion
print(10 % 3)  # Modulo
print(10 // 3) # Division baja

# Ejemplo:

numero_1 = 6
numero_2 = 3

suma = numero_1 + numero_2
resta = numero_1 - numero_2
multi = numero_1 * numero_2
division = numero_1 / numero_2
exponente = numero_1 ** numero_2
