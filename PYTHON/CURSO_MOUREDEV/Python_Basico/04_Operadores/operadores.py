### Operadores comparativos ###

print(5 > 3)
print(3 < 5)
print(5 >= 3)
print(3 <= 5)
print(5 == 3)
print(5 != 3)


### Operadores Logicos: ###

print(3 > 4 and 3 > 4)
print(3 < 4 and 3 <4 )

print(3 > 4 or 3 < 4)

print(not(3<4)) # Negar la comparacion.